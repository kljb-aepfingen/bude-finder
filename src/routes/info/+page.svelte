						class="underline text-sky-400"
						target="_blank">Github</a
					> erstellen.
				</p>
			</li>
			<li>
				<h2 id="daten">Eure Daten</h2>
				<p>Welche Daten speichern wir und welche nicht:</p>
				<ol class="counter-alph">
					<li>
						<h3 id="daten-standort">Dein Standort</h3>
						wird nur local auf deinem Handy gespeichert und wird dazu genutzt dir deinen Standort auf
						der Karte anzeigen zu lassen. Wir senden<b>keine</b> Standorte zu unseren Servern und
						speichern sie somit auch <b>nicht</b>.
					</li>
					<li>
						<h3 id="daten-anmeldung">Deine Anmelde Daten</h3>
						bekommen wir von Google und speichern somit deine Google Email, dein Google Name und den
						Link zu deinem Google Profilbild, falls du eins hast. Die wird allerdings nirgends auf unserer
						Seite angezeigt und ist nur für unseren Admin sichtbar.
					</li>
					<li>
						<h3 id="daten-bude">Deine Bude Daten</h3>
						speichern wir natürlich komplet auf unseren Servern. Dabei ist wichtig dass deine angegebene
						Kontaktinformation<b>nicht</b> für andere sichtbar ist, außer für unseren Admin. Ihr werdet
						eventuel darüber kontaktiert, falls eure Bude/Landjugend gemeldet wird.
					</li>
					<li>
						<h3 id="daten-bewertung">Deine Bewertungen</h3>
						sind natürlich für alle sichtbar, es sieht aber keine wer diese Bewertungen gemacht hat,
						außer unser Admin.
					</li>
					<li>
						<h3 id="daten-meldung">Deine Meldungen</h3>
						sind<b>nicht</b> anonym. Die gemeldete Bude/Landjugend kann allerdings nicht sehen von
						wem sie gemeldet wurde. Lediglich unser Admin sieht wer du bist, zuminderst sieht er
						dein Google Name. Dies wird aus mehreren Gründen gemacht:
						<ul class="list-disc ml-3">
							<li>Um zu verhindern dass jemand die gleiche Bude/Landjugen mehrmals meldet</li>
							<li>Um zu sehen ob jemand wahrlos Buden/Landjugenden meldet</li>
							<li>Um die Möglichkeit zu bieten eigene Meldungen wieder zu löschen</li>
						</ul>
						Auch die angegebenen Daten in einer Meldung kann nur unser Admin sehen und setzt sich dann
						gegebenenfalls mit dem Melder und/oder der gemeldeten Bude/Landjugend in verbindung.
					</li>
				</ol>
			</li>
