					befinden. Deshalb haben wir diese App entwickelt, sodass sich jede Bude/Landjugend
					eintragen kann. Wenn ihr eine Bude habt einfach mit Google anmelden und eure
					Bude/Landjugend auf der Karte markieren. Ganz einfach
				</p>
			</li>
			<li>
				<h2 id="feedback">Feedback</h2>
				<p>
					Wenn ihr irgendwelche Fehler in unserer App entdeckt, egal ob Schreibfehler oder nicht
